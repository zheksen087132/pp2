#1 Count from 1 to 5
for i in range(1, 6):
    print(i)

#2 Sum numbers up to 10
s = 0
for i in range(1, 11):
    s += i
print("Sum =", s)

#3 Iterate over list
for fruit in ["apple", "banana", "cherry"]:
    print(fruit)

#4 Print squares
for i in range(1, 6):
    print(i, i*i)

#5 Iterate over string
for ch in "hello":
    print(ch)